import { Model, DataTypes } from 'sequelize';
import db from '.';
// import Matches from './Matches';

class Teams extends Model {
  public id!: number;

  public teamName!: string;
}

// Teams.hasMany(Matches, { foreignKey: 'id', as: 'home_team' });
